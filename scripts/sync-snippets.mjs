/**
 * Contract: committed partials and the in-memory --check output provide one
 * byte-diff mechanism that catches drift in both directions: a source changed
 * after generation, or a partial was edited directly. The same check detects
 * missing and orphaned partials. CI runs --check only (never write mode), so
 * this authoritative gate cannot be defeated by merely regenerating and
 * force-committing over real drift undetected.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDirectory, '..');
const snippetsRoot = path.join(repoRoot, 'website', 'snippets');
const examplesOutputDirectory = path.join(snippetsRoot, 'examples');
const readmeOutputDirectory = path.join(snippetsRoot, 'readme');

const readmeManifest = new Map([
  ['millipede', 'millipede/README.md'],
  ['millipede-core', 'crates/millipede-core/README.md'],
  ['millipede-http', 'crates/millipede-http/README.md'],
  ['millipede-html', 'crates/millipede-html/README.md'],
  ['millipede-browser', 'crates/millipede-browser/README.md'],
  [
    'millipede-browser-chromiumoxide',
    'crates/millipede-browser-chromiumoxide/README.md',
  ],
  ['millipede-fingerprint', 'crates/millipede-fingerprint/README.md'],
  ['millipede-storage-memory', 'crates/millipede-storage-memory/README.md'],
  ['millipede-storage-fs', 'crates/millipede-storage-fs/README.md'],
]);

function readLfText(relativePath) {
  return fs.readFileSync(path.join(repoRoot, relativePath), 'utf8').replace(/\r\n?/g, '\n');
}

function withOneTrailingNewline(content) {
  return `${content.replace(/\n+$/g, '')}\n`;
}

function fenceFor(content) {
  let longestRun = 0;
  for (const match of content.matchAll(/`+/g)) {
    longestRun = Math.max(longestRun, match[0].length);
  }
  return '`'.repeat(Math.max(4, longestRun + 1));
}

function renderPartial(sourcePath, title, content) {
  const normalizedContent = withOneTrailingNewline(content);
  const fence = fenceFor(normalizedContent);
  return (
    `{/* AUTO-GENERATED by scripts/sync-snippets.mjs from ${sourcePath}. Do not edit; run: node scripts/sync-snippets.mjs */}\n\n` +
    `${fence}rust title="${title}"\n` +
    normalizedContent +
    `${fence}\n`
  );
}

function extractSingleRustFence(readmePath, readme) {
  const lines = readme.split('\n');
  const rustFences = [];

  for (let openingIndex = 0; openingIndex < lines.length; openingIndex += 1) {
    if (!/^```rust\b/.test(lines[openingIndex])) continue;

    let closingIndex = openingIndex + 1;
    while (closingIndex < lines.length && !/^`{3,}[ \t]*$/.test(lines[closingIndex])) {
      closingIndex += 1;
    }
    if (closingIndex === lines.length) {
      throw new Error(`${readmePath} has an unterminated Rust fence`);
    }

    rustFences.push(lines.slice(openingIndex + 1, closingIndex));
    openingIndex = closingIndex;
  }

  if (rustFences.length !== 1) {
    throw new Error(
      `${readmePath} must contain exactly one Rust fence; found ${rustFences.length}`,
    );
  }

  return rustFences[0]
    .filter((line) => !/^[ \t]*#(?:$| )/.test(line))
    .join('\n');
}

function generateExpectedPartials() {
  const expected = new Map();
  const examplesRelativeDirectory = 'millipede/examples';
  const examplesDirectory = path.join(repoRoot, examplesRelativeDirectory);
  const exampleFiles = fs
    .readdirSync(examplesDirectory, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith('.rs'))
    .map((entry) => entry.name)
    .sort();

  for (const fileName of exampleFiles) {
    const stem = fileName.slice(0, -'.rs'.length);
    const sourcePath = `${examplesRelativeDirectory}/${fileName}`;
    const outputPath = path.join(examplesOutputDirectory, `${stem}.mdx`);
    expected.set(
      outputPath,
      renderPartial(sourcePath, sourcePath, readLfText(sourcePath)),
    );
  }

  for (const [name, sourcePath] of readmeManifest) {
    const readme = readLfText(sourcePath);
    const content = extractSingleRustFence(sourcePath, readme);
    const outputPath = path.join(readmeOutputDirectory, `${name}.mdx`);
    expected.set(
      outputPath,
      renderPartial(sourcePath, `${sourcePath} (doc-tested)`, content),
    );
  }

  return expected;
}

function mdxFilesIn(directory) {
  if (!fs.existsSync(directory)) return [];
  const files = [];
  const entries = fs
    .readdirSync(directory, { withFileTypes: true })
    .sort((left, right) => left.name.localeCompare(right.name));
  for (const entry of entries) {
    const entryPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...mdxFilesIn(entryPath));
    } else if (entry.isFile() && entry.name.endsWith('.mdx')) {
      files.push(entryPath);
    }
  }
  return files;
}

function relative(filePath) {
  return path.relative(repoRoot, filePath).split(path.sep).join('/');
}

function scanUnmanagedRustFences(directory, findings) {
  if (!fs.existsSync(directory)) return;

  const entries = fs
    .readdirSync(directory, { withFileTypes: true })
    .sort((left, right) => left.name.localeCompare(right.name));
  for (const entry of entries) {
    const entryPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      scanUnmanagedRustFences(entryPath, findings);
    } else if (entry.isFile() && entry.name.endsWith('.mdx')) {
      const lines = fs.readFileSync(entryPath, 'utf8').replace(/\r\n?/g, '\n').split('\n');
      lines.forEach((line, index) => {
        if (/^[ \t]*`{3,}(?:rust|rs)\b/.test(line)) {
          findings.push(
            `UNMANAGED RUST FENCE: ${relative(entryPath)}:${index + 1} — ` +
              'move the Rust code into a compiled source and include the generated partial',
          );
        }
      });
    }
  }
}

function check(expected) {
  const findings = [];
  for (const [outputPath, generatedContent] of expected) {
    if (!fs.existsSync(outputPath)) {
      findings.push(
        `MISSING: ${relative(outputPath)} — run: node scripts/sync-snippets.mjs and commit`,
      );
    } else if (!fs.readFileSync(outputPath).equals(Buffer.from(generatedContent, 'utf8'))) {
      findings.push(
        `STALE: ${relative(outputPath)} — run: node scripts/sync-snippets.mjs and commit`,
      );
    }
  }

  for (const outputPath of [
    ...mdxFilesIn(examplesOutputDirectory),
    ...mdxFilesIn(readmeOutputDirectory),
  ]) {
    if (!expected.has(outputPath)) {
      findings.push(
        `ORPHAN: ${relative(outputPath)} — run: node scripts/sync-snippets.mjs and commit`,
      );
    }
  }

  scanUnmanagedRustFences(path.join(repoRoot, 'website', 'content', 'docs'), findings);
  for (const finding of findings) console.error(finding);
  return findings.length === 0;
}

function write(expected) {
  fs.mkdirSync(examplesOutputDirectory, { recursive: true });
  fs.mkdirSync(readmeOutputDirectory, { recursive: true });

  for (const [outputPath, generatedContent] of expected) {
    fs.writeFileSync(outputPath, generatedContent, 'utf8');
  }

  for (const outputPath of [
    ...mdxFilesIn(examplesOutputDirectory),
    ...mdxFilesIn(readmeOutputDirectory),
  ]) {
    if (!expected.has(outputPath)) fs.unlinkSync(outputPath);
  }
}

const arguments_ = process.argv.slice(2);
if (arguments_.length > 1 || (arguments_.length === 1 && arguments_[0] !== '--check')) {
  throw new Error('usage: node scripts/sync-snippets.mjs [--check]');
}

const expected = generateExpectedPartials();
if (arguments_[0] === '--check') {
  if (!check(expected)) process.exitCode = 1;
} else {
  write(expected);
}
